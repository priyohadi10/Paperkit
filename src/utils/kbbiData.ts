// Create a Set for O(1) lookups
const kbbiWordSet = new Set(kbbiWords.map(w => w.toLowerCase()));

export function isValidKBBIWord(word: string): boolean {
  return kbbiWordSet.has(word.toLowerCase().trim());
}

export function isWordUsed(word: string, usedWords: string[]): boolean {
  return usedWords.some(w => w.toLowerCase() === word.toLowerCase().trim());
}

export function getLastLetter(word: string): string {
  const clean = word.toLowerCase().trim();
  if (clean.length === 0) return '';

  // For Indonesian, we take the last letter
  const lastChar = clean[clean.length - 1];

  // Common Indonesian suffixes that change the starting letter
  // -kan -> n, -i -> i, -an -> n, -lah -> h, -kah -> h, -nya -> a, -ku -> u, -mu -> u
  return lastChar;
}

export function doesWordStartWith(word: string, letter: string): boolean {
  return word.toLowerCase().trim().startsWith(letter.toLowerCase());
}
